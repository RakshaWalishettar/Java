class Beer{
	public static void brewing(String grains)
	{
		System.out.println("drinking");
		System.out.println(grains);
	}
	public static void pouring(String aroma)
	{
		System.out.println("glass");
		System.out.println(aroma);
	}
	public static void pairing(String flavors)
	{
		System.out.println("varity");
		System.out.println(flavors);
	}
	public static void filtering(int liter,String separate)
	{
		System.out.println("clarity");
		System.out.println(liter);
		System.out.println(separate);
	}
	public static void mashing(boolean yesorno,int count)
	{
		System.out.println("starches");
		System.out.println(yesorno);
		System.out.println(count);
	}
	public static void packaging(double cost,String types,char p)
	{
		System.out.println("different");
		System.out.println(cost);
		System.out.println(types);
		System.out.println(p);
	}
	public static void storing(boolean yesorno,int date,String exp)
	{
		System.out.println("maintain");
		System.out.println(yesorno);
		System.out.println(date);
		System.out.println(exp);
	}	
}