class Double{
	public static void main(String[] args){
		double temperature=1.25;
			System.out.println(temperature);
		double height=5.3;
			System.out.println(height);
		double weight=54.5;
			System.out.println(weight);
		double speed=24.5;
			System.out.println(speed);
			
		System.out.println("after re-assign");
		temperature=-1.25;
			System.out.println(temperature);
		height=4.9;
			System.out.println(height);
		weight=66.9;
			System.out.println(weight);
		speed=50.4;
			System.out.println(speed);
	}
}